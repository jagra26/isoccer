package com.company;

public class Pessoa {
    String nome;
    String email;
    int cpf;
    int telefone;
    double salario;
    public Pessoa(String nome, String email, int cpf, int telefone, double salario){
        this.nome = nome;
        this.email = email;
        this.cpf = cpf;
        this.telefone = telefone;
        this.salario = salario;
    }
}